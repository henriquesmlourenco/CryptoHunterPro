func importUnmixerSeedExcel() []string {
	fmt.Println()
	fmt.Println("================================================================")
	fmt.Println("  " + t("IMPORTAR EXCEL DO UNMIXER SEED", "IMPORT UNMIXER SEED EXCEL"))
	fmt.Println("================================================================")
	fmt.Println()

	importDir := t("Importar_Seeds", "Import_Seeds")
	if _, err := os.Stat(importDir); os.IsNotExist(err) {
		os.MkdirAll(importDir, 0755)
		fmt.Println(t(
			"  Pasta criada: "+importDir,
			"  Folder created: "+importDir))
		fmt.Println()
		fmt.Println(t(
			"  Copie os arquivos Excel (.xlsx) gerados pelo Unmixer Seed",
			"  Copy the Excel files (.xlsx) generated by Unmixer Seed"))
		fmt.Println(t(
			"  para esta pasta e pressione Enter para continuar.",
			"  to this folder and press Enter to continue."))
		fmt.Println()
		getUserInput(t("  Pressione Enter quando estiver pronto...", "  Press Enter when ready..."))
	}

	files, err := filepath.Glob(filepath.Join(importDir, "*.xlsx"))
	if err != nil || len(files) == 0 {
		fmt.Println(t(
			"  [!] Nenhum arquivo .xlsx encontrado na pasta "+importDir,
			"  [!] No .xlsx files found in folder "+importDir))
		return nil
	}

	fmt.Printf(t(
		"  Encontrados %d arquivo(s) Excel:\n",
		"  Found %d Excel file(s):\n"), len(files))
	fmt.Println()

	var allSeeds []string

	for _, file := range files {
		fmt.Printf("  - %s", filepath.Base(file))

		f, err := excelize.OpenFile(file)
		if err != nil {
			fmt.Printf(" [%s]\n", t("ERRO ao abrir", "ERROR opening"))
			continue
		}

		sheets := f.GetSheetList()
		if len(sheets) == 0 {
			fmt.Printf(" [%s]\n", t("sem sheets", "no sheets"))
			f.Close()
			continue
		}

		rows, err := f.GetRows(sheets[0])
		if err != nil {
			fmt.Printf(" [%s]\n", t("ERRO ao ler", "ERROR reading"))
			f.Close()
			continue
		}

		seedCount := 0
		for i, row := range rows {
			if i == 0 {
				continue
			}
			if len(row) >= 2 {
				seed := strings.TrimSpace(row[1])
				if seed != "" {
					words := strings.Fields(seed)
					if len(words) >= 12 {
						allSeeds = append(allSeeds, seed)
						seedCount++
					}
				}
			}
		}

		fmt.Printf(" -> %d seeds\n", seedCount)
		f.Close()
	}

	fmt.Println()
	fmt.Printf(t(
		"  Total de seeds importadas: %d\n",
		"  Total seeds imported: %d\n"), len(allSeeds))

	return allSeeds
}

