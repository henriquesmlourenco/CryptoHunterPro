func generateExcelReport(results []ScanResult) string {
	timestamp := time.Now().Format("20060102_150405")

	dirName := t(
		fmt.Sprintf("Resultados_%s", timestamp),
		fmt.Sprintf("Results_%s", timestamp))
	os.MkdirAll(dirName, 0755)

	fileName := t(
		fmt.Sprintf("Relatorio_Saldos_%s.xlsx", timestamp),
		fmt.Sprintf("Balance_Report_%s.xlsx", timestamp))
	filePath := filepath.Join(dirName, fileName)

	f := excelize.NewFile()

	// Headers com coluna Data Ultima Movimentacao
	headers := []string{
		t("Seed Phrase", "Seed Phrase"),
		t("Rede", "Network"),
		t("Derivacao", "Derivation"),
		t("Indice", "Index"),
		t("Endereco", "Address"),
		t("Saldo Nativo", "Native Balance"),
		t("Simbolo", "Symbol"),
		t("Tokens", "Tokens"),
		t("Historico", "History"),
		t("Qtd Transacoes", "Tx Count"),
		t("Data Ultima Movimentacao", "Last Transaction Date"),
		t("Chave Privada", "Private Key"),
	}

	// Estilo do header
	headerStyle, _ := f.NewStyle(&excelize.Style{
		Font:      &excelize.Font{Bold: true, Color: "FFFFFF", Size: 11},
		Fill:      excelize.Fill{Type: "pattern", Color: []string{"2F5496"}, Pattern: 1},
		Alignment: &excelize.Alignment{Horizontal: "center"},
		Border: []excelize.Border{
			{Type: "left", Color: "000000", Style: 1},
			{Type: "top", Color: "000000", Style: 1},
			{Type: "bottom", Color: "000000", Style: 1},
			{Type: "right", Color: "000000", Style: 1},
		},
	})

	balanceRowStyle, _ := f.NewStyle(&excelize.Style{
		Fill: excelize.Fill{Type: "pattern", Color: []string{"C6EFCE"}, Pattern: 1},
		Font: &excelize.Font{Bold: true},
	})

	historyRowStyle, _ := f.NewStyle(&excelize.Style{
		Fill: excelize.Fill{Type: "pattern", Color: []string{"FFF2CC"}, Pattern: 1},
	})

	colWidths := map[string]float64{
		"A": 40, "B": 15, "C": 22, "D": 8,
		"E": 50, "F": 20, "G": 8, "H": 40,
		"I": 10, "J": 14, "K": 24, "L": 70,
	}

	// ====== ABA 1: RESUMO (todos os resultados) ======
	summarySheet := t("Resumo", "Summary")
	f.SetSheetName("Sheet1", summarySheet)
	writeSheetHeaders(f, summarySheet, headers, headerStyle)
	writeSheetData(f, summarySheet, results, 2, balanceRowStyle, historyRowStyle, false, false)
	for col, width := range colWidths {
		f.SetColWidth(summarySheet, col, col, width)
	}

	// ====== ABA 2: COM SALDO (apenas enderecos com saldo atual) ======
	balanceSheet := t("Com Saldo", "With Balance")
	f.NewSheet(balanceSheet)
	writeSheetHeaders(f, balanceSheet, headers, headerStyle)
	writeSheetData(f, balanceSheet, results, 2, balanceRowStyle, historyRowStyle, true, false)
	for col, width := range colWidths {
		f.SetColWidth(balanceSheet, col, col, width)
	}

	// ====== ABA 3: COM HISTORICO (enderecos com historico mas sem saldo atual) ======
	historySheet := t("Com Historico", "With History")
	f.NewSheet(historySheet)
	writeSheetHeaders(f, historySheet, headers, headerStyle)
	writeSheetData(f, historySheet, results, 2, balanceRowStyle, historyRowStyle, false, true)
	for col, width := range colWidths {
		f.SetColWidth(historySheet, col, col, width)
	}

	// Salvar
	if err := f.SaveAs(filePath); err != nil {
		fmt.Printf(t(
			"  [!] Erro ao salvar Excel: %v\n",
			"  [!] Error saving Excel: %v\n"), err)
		return ""
	}

	return filePath
}

func writeSheetHeaders(f *excelize.File, sheet string, headers []string, style int) {
	for i, h := range headers {
		cell := fmt.Sprintf("%c1", 'A'+i)
		f.SetCellValue(sheet, cell, h)
	}
	f.SetCellStyle(sheet, "A1", fmt.Sprintf("%c1", 'A'+len(headers)-1), style)
}

func writeSheetData(f *excelize.File, sheet string, results []ScanResult, startRow int, balStyle int, histStyle int, onlyBalance bool, onlyHistory bool) {
	row := startRow
	for _, r := range results {
		// Filtrar conforme a aba
		if onlyBalance && !r.HasBalance {
			continue
		}
		if onlyHistory && (!r.HasHistory || r.HasBalance) {
			// Aba "Com Historico" = tem historico mas NAO tem saldo atual
			continue
		}

		tokenStr := ""
		for _, tok := range r.Tokens {
			if tokenStr != "" {
				tokenStr += " | "
			}
			tokenStr += fmt.Sprintf("%s: %s", tok.Symbol, tok.Balance)
		}

		historyStr := t("Nao", "No")
		if r.HasHistory {
			historyStr = t("Sim", "Yes")
		}

		lastTxDate := r.LastTxDate
		if lastTxDate == "" {
			lastTxDate = "-"
		}

		txCountStr := "-"
		if r.TxCount > 0 {
			txCountStr = fmt.Sprintf("%d", r.TxCount)
		}

		f.SetCellValue(sheet, fmt.Sprintf("A%d", row), r.SeedPhrase)
		f.SetCellValue(sheet, fmt.Sprintf("B%d", row), r.Network)
		f.SetCellValue(sheet, fmt.Sprintf("C%d", row), r.DerivationPath)
		f.SetCellValue(sheet, fmt.Sprintf("D%d", row), r.Index)
		f.SetCellValue(sheet, fmt.Sprintf("E%d", row), r.Address)
		f.SetCellValue(sheet, fmt.Sprintf("F%d", row), r.NativeBalance)
		f.SetCellValue(sheet, fmt.Sprintf("G%d", row), r.NativeSymbol)
		f.SetCellValue(sheet, fmt.Sprintf("H%d", row), tokenStr)
		f.SetCellValue(sheet, fmt.Sprintf("I%d", row), historyStr)
		f.SetCellValue(sheet, fmt.Sprintf("J%d", row), txCountStr)
		f.SetCellValue(sheet, fmt.Sprintf("K%d", row), lastTxDate)
		f.SetCellValue(sheet, fmt.Sprintf("L%d", row), r.PrivateKey)

		// Destacar linhas
		if r.HasBalance {
			f.SetCellStyle(sheet, fmt.Sprintf("A%d", row), fmt.Sprintf("L%d", row), balStyle)
		} else if r.HasHistory {
			f.SetCellStyle(sheet, fmt.Sprintf("A%d", row), fmt.Sprintf("L%d", row), histStyle)
		}

		row++
	}
}
